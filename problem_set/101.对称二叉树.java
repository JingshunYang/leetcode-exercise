/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
  public boolean isSymmetric(TreeNode root) {
    return root == null || checkSymmetric(root.left, root.right);
  }
  public boolean checkSymmetric(TreeNode node_1, TreeNode node_2) {
    if (node_1 == null && node_2 == null) {
      return true;
    } else if (node_1 != null && node_2 != null) {
      return node_1.val == node_2.val 
        && checkSymmetric(node_1.left, node_2.right)
        && checkSymmetric(node_1.right, node_2.left);
    }
    return false;
  }
}
